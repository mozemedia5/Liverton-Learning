/**
 * Get emoji category by name
 * @param categoryName - Name of the category
 * @returns EmojiCategory or undefined
 */
export function getEmojiCategory(categoryName: string): EmojiCategory | undefined {
  return EMOJI_CATEGORIES.find((cat) => cat.name === categoryName);
}

/**
 * Get all emoji category names
 * @returns Array of category names
 */
export function getEmojiCategoryNames(): string[] {
  return EMOJI_CATEGORIES.map((cat) => cat.name);
}

/**
 * Search emojis by keyword
 * @param keyword - Search keyword
 * @returns Array of matching emojis
 */
export function searchEmojis(keyword: string): string[] {
  const lowerKeyword = keyword.toLowerCase();
  const results: string[] = [];

  EMOJI_CATEGORIES.forEach((category) => {
    if (category.name.toLowerCase().includes(lowerKeyword)) {
      results.push(...category.emojis);
    }
  });

  return results;
}
